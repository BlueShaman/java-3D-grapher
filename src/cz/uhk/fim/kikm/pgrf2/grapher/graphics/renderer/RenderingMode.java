package cz.uhk.fim.kikm.pgrf2.grapher.graphics.renderer;

/**
 * RenderingMode enum is used to distinguish different types of rendering modes.
 * 3 modes are available:
 *   1. Wireframe
 *   2. Solid
 *   3. Both together
 * Created by Jakub Kol on 7.4.16.
 */
public enum RenderingMode {
    MODE_WIREFRAME,
    MODE_SOLID,
    MODE_WIREFRAME_SOLID
}
